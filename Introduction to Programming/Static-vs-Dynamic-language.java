// Static vs Dynamic Programming Languages



// 1. Static Language
// - Variable ka data type pehle define karna padta hai.
// - Type checking compile time par hoti hai.
// - Errors jaldi mil jati hain.
// - Fast aur secure hoti hai.

// Example:
//  int age = 20;

// Languages:
// Java, C, C++



// 2. Dynamic Language
// - Variable ka type automatically decide hota hai.
// - Type checking runtime par hoti hai.
// - Flexible hoti hai but runtime errors aa sakte hain.

// Example:
// age = 20

// Languages:
// Python, JavaScript